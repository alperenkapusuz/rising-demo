'use client';
import { toast } from 'react-toastify';

//Toast messages are setting functions

export const toastSuccess = (message: string) => {
  return toast.success(message, {
    position: 'top-right',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'colored',
  });
};

